#include "3-calc.h"
/**
 * op_add - entry point
 * Descriprion: add 2 integers
 * @a:integer
 * @b:integer
 * Return: result of type integer
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - entry point
 * Descriprion: subtract 2 integers
 * @a:integer
 * @b:integer
 * Return: result of type integer
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - entry point
 * Descriprion: multipy 2 integers
 * @a:integer
 * @b:integer
 * Return: result of type integer
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - entry point
 * Descriprion: divide 2 integers
 * @a:integer
 * @b:integer
 * Return: result of type integer
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - entry point
 * Descriprion: modulo
 * @a:integer
 * @b:integer
* Return: result of type integer
*/
int op_mod(int a, int b)
{
	return (a % b);
}
